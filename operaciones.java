package ejercicio6;

import java.util.*;


public class operaciones{
	public int n1;
	public int n2;


public double suma(double n1, double n2){
	double resultado = 0;
	resultado = n1 + n2;
	return resultado;
	
}

public double resta(double n1, double n2){
	double resultado = 0;
	resultado = n1 - n2;
	return resultado;
	
}

public double producto(double n1, double n2){
	double resultado = 0;
	resultado = n1 * n2;
	return resultado;
	
}

public double division(double n1, double n2){
	double resultado = 0;
	resultado = n1 / n2;
	return resultado;
	
 }
}